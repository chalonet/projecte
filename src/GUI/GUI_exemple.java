    private void boto_inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boto_inserirActionPerformed
        // TODO add your handling code here:
        if (!dadesCorrectes()) {
            JOptionPane.showMessageDialog(this, "Dades de les caselles incorrectes!!");
            return;
        }
        if (casellabuida == MAX_JUGADORS) {
            JOptionPane.showMessageDialog(this, "Has introduït el màxim de jugadors");
        } else {

            Jugador[] array = Projecte.getArray();

            array[casellabuida].setNom(casellaNom.getText().trim());
            array[casellabuida].setNacionalitat(casellaNacionalitat.getText().trim());
            array[casellabuida].setEquip(casellaEquip.getText().trim());
            array[casellabuida].setPes(Integer.valueOf(casellaPes.getText()));
            array[casellabuida].setMida(Double.valueOf(casellaMida.getText()));
            array[casellabuida].setSalari(Double.valueOf(casellaSalari.getText()));
            array[casellabuida].setTitular(opcio_titular.isSelected());
            array[casellabuida].setOmplert(true);
            casellabuida++;

            GUI_UF3.carregaTaula(new String[]{"Fila", "Nom", "Nacionalitat", "Equip", "Pes", "Alçada", "Salari", "Titular"},
                    transformaDades(Projecte.getArray()), Taula);
            JOptionPane.showMessageDialog(this, "Dades introduides");
        }
        //Poso les caselles en blanc.
        borrarcaselles();
    }//GEN-LAST:event_boto_inserirActionPerformed

    private void boto_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boto_borrarActionPerformed
        //TODO add your handling code here:

        boto_borrar.setEnabled(false);
        boto_modificar.setEnabled(false);

        int iArray = (int) Taula.getValueAt(filaSel, 0);
        Jugador[] array = Projecte.getArray();
        casellabuida--;
        if (filaSel + 1 == MAX_JUGADORS) {
            array[iArray].setOmplert(false);

        } else {
            for (int i = filaSel; i < casellabuida; i++) {
                array[i] = array[i + 1];
            }
            array[casellabuida] = new Jugador();
            array[casellabuida].setOmplert(false);
        }
        GUI_UF3.carregaTaula(new String[]{"Fila", "Nom", "Nacionalitat", "Equip", "Pes", "Alçada", "Salari", "Titular"},
                transformaDades(Projecte.getArray()), Taula);
        JOptionPane.showMessageDialog(this, "Dades borrades");

        //Poso les caselles en blanc.
        borrarcaselles();
    }//GEN-LAST:event_boto_borrarActionPerformed

