    public static Object[][] transformaDades(Jugador[] dades) {
        Object[][] resultat = new Object[dades.length][8];
        //Variable per comptar el número de pilots en dades que hi ha a l'array de dades
        int omplerts = 0;

        for (int i = 0; i < dades.length; i++) {
            if (dades[i].isOmplert()) {
                resultat[omplerts][0] = i;
                resultat[omplerts][1] = dades[i].getNom();
                resultat[omplerts][2] = dades[i].getNacionalitat();
                resultat[omplerts][3] = dades[i].getEquip();
                resultat[omplerts][4] = dades[i].getPes();
                resultat[omplerts][5] = dades[i].getMida();
                resultat[omplerts][6] = dades[i].getSalari();
                resultat[omplerts][7] = dades[i].isTitular();
                omplerts++;
            }
        }
        //Retornem només els pilots plens
        return Arrays.copyOf(resultat, omplerts);
    }

