    private void borrarcaselles() {
        casellaNom.setText("");
        casellaNacionalitat.setText("");
        casellaEquip.setText("");
        casellaPes.setText("");
        casellaMida.setText("");
        casellaSalari.setText("");
        opcio_titular.setSelected(true);
    }

    private void TaulaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TaulaMouseClicked
        //Actualitzo la fila seleccionada.
        filaSel = Taula.getSelectedRow();

        if (filaSel == -1) {
            //Si no han selecionat cap fila desactivo els botons de borrar i modificar.
            boto_borrar.setEnabled(false);
            boto_modificar.setEnabled(false);

            //Poso les caselles en blanc.
            borrarcaselles();
        } else {
            //Si no han selecionat cap fila desactivo els botons de borrar i modificar.
            boto_borrar.setEnabled(true);
            boto_modificar.setEnabled(true);

            //Actualitzo les caselles en la informacio de la fila selecionada
            casellaNom.setText((String) Taula.getValueAt(filaSel, 1));
            casellaNacionalitat.setText((String) Taula.getValueAt(filaSel, 2));
            casellaEquip.setText((String) Taula.getValueAt(filaSel, 3));
            casellaPes.setText(String.valueOf(Taula.getValueAt(filaSel, 4)));
            casellaMida.setText(String.valueOf(Taula.getValueAt(filaSel, 5)));
            casellaSalari.setText(String.valueOf(Taula.getValueAt(filaSel, 6)));
            if ((boolean) Taula.getValueAt(filaSel, 7)) {
                opcio_titular.setSelected(true);
            } else {
                opcio_no_titular.setSelected(true);
            }
        }
    }//GEN-LAST:event_TaulaMouseClicked

    private boolean dadesCorrectes() {

        try {
            if (casellaNom.getText().trim().equals("")) {
                throw new StringIndexOutOfBoundsException();
            }
            if (casellaNacionalitat.getText().trim().equals("")) {
                throw new StringIndexOutOfBoundsException();
            }

            Integer.valueOf(casellaPes.getText().trim());
            Double.valueOf(casellaMida.getText());
            Double.valueOf(casellaSalari.getText());

        } catch (StringIndexOutOfBoundsException | NumberFormatException e) {
            return false;
        }

        return true;
    }

    private void boto_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boto_modificarActionPerformed

        boto_modificar.setEnabled(false);
        boto_borrar.setEnabled(false);
        //Primer miro si les dades de les caselles són correctes
        if (!dadesCorrectes()) {
            JOptionPane.showMessageDialog(this, "Dades de les caselles incorrectes!!");
            return;
        }
        //Obtenim l'índex de l'array apartir del valor de la columna 0 de la taula.
        int iArray = (int) Taula.getValueAt(filaSel, 0);
        Jugador[] array = Projecte.getArray();

        array[iArray].setNom(casellaNom.getText().trim());
        array[iArray].setNacionalitat(casellaNacionalitat.getText().trim());
        array[iArray].setEquip(casellaEquip.getText().trim());
        array[iArray].setPes(Integer.valueOf(casellaPes.getText()));
        array[iArray].setMida(Double.valueOf(casellaMida.getText()));
        array[iArray].setSalari(Double.valueOf(casellaSalari.getText()));
        array[iArray].setTitular(opcio_titular.isSelected());

        GUI_UF3.carregaTaula(new String[]{"Fila", "Nom", "Nacionalitat", "Equip", "Pes", "Alçada", "Salari", "Titular"},
                transformaDades(Projecte.getArray()), Taula);
        JOptionPane.showMessageDialog(this, "Dades modificades");

        //Poso les caselles en blanc.
        borrarcaselles();
    }//GEN-LAST:event_boto_modificarActionPerformed

